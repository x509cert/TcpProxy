fn obfuscate(input: &str) -> String {
    input.chars()
        .map(|ch| {
            MAPPINGS.get(&ch)
                .and_then(|mapping| mapping.chars().collect::<Vec<_>>().choose(&mut rand::thread_rng()).cloned())
                .unwrap_or(ch)
        })
        .collect()
}

